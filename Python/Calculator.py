Calculator = input('Enter +,-,*,/,')
First = input('Enter Number(1)')
Second = input('Enter Number(2)')



if Calculator == '+':
    Finished = int(First) + int(Second)
    print(Finished)
elif Calculator == '-':
     Finished = int(First) - int(Second)
     print(Finished)
elif Calculator == '*':
    Finished = int(First) * int(Second)
    print(Finished)
elif Calculator == '/':
    Finished = int(First) / int(Second)
    print(Finished)
